'use client'

import { useEffect, useState, useCallback } from 'react'
import { useParams, useRouter } from 'next/navigation'
import { useSession } from 'next-auth/react'
import { Post, User, Tag } from '@prisma/client'
import ReactMarkdown from 'react-markdown'
import remarkBreaks from 'remark-breaks'
import XShareButton from '@/components/ui/XShareButton'
import styles from './post-detail.module.css'

interface TocItem {
  id: string
  text: string
  level: number
}

type PostWithDetails = Post & {
  user: User
  postTags: Array<{
    tag: Tag
  }>
  _count: {
    likes: number
  }
}

export default function PostDetailPage() {
  const params = useParams()
  const router = useRouter()
  const { data: session } = useSession()
  const [post, setPost] = useState<PostWithDetails | null>(null)
  const [loading, setLoading] = useState(true)
  const [liked, setLiked] = useState(false)
  const [bookmarked, setBookmarked] = useState(false)
  const [currentUrl, setCurrentUrl] = useState('')
  const [tocItems, setTocItems] = useState<TocItem[]>([])

  // ÁõÆÊ¨°„Ç¢„Ç§„ÉÜ„É†„ÇíÁîüÊàê„Åô„ÇãÈñ¢Êï∞
  const generateTocItems = useCallback((content: string) => {
    const headingRegex = /^(#{1,6})\s+(.+)$/gm
    const items: TocItem[] = []
    let match
    
    while ((match = headingRegex.exec(content)) !== null) {
      const level = match[1].length
      const text = match[2].trim()
      const id = text
        .toLowerCase()
        .replace(/[^\w\s-]/g, '')
        .replace(/\s+/g, '-')
      
      items.push({ id, text, level })
    }
    
    return items
  }, [])

  // „Ç´„Çπ„Çø„É†Markdown„Ç≥„É≥„Éù„Éº„Éç„É≥„Éà
  const MarkdownComponents = {
    h1: ({ children }: { children: React.ReactNode }) => {
      const text = String(children)
      const id = text.toLowerCase().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-')
      return <h1 id={id}>{children}</h1>
    },
    h2: ({ children }: { children: React.ReactNode }) => {
      const text = String(children)
      const id = text.toLowerCase().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-')
      return <h2 id={id}>{children}</h2>
    },
    h3: ({ children }: { children: React.ReactNode }) => {
      const text = String(children)
      const id = text.toLowerCase().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-')
      return <h3 id={id}>{children}</h3>
    },
    h4: ({ children }: { children: React.ReactNode }) => {
      const text = String(children)
      const id = text.toLowerCase().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-')
      return <h4 id={id}>{children}</h4>
    },
    h5: ({ children }: { children: React.ReactNode }) => {
      const text = String(children)
      const id = text.toLowerCase().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-')
      return <h5 id={id}>{children}</h5>
    },
    h6: ({ children }: { children: React.ReactNode }) => {
      const text = String(children)
      const id = text.toLowerCase().replace(/[^\w\s-]/g, '').replace(/\s+/g, '-')
      return <h6 id={id}>{children}</h6>
    },
  }

  useEffect(() => {
    setCurrentUrl(window.location.origin)
  }, [])

  const checkLikeAndBookmarkStatus = useCallback(async () => {
    try {
      const [likeResponse, bookmarkResponse] = await Promise.all([
        fetch(`/api/posts/${params.id}/like-status`),
        fetch(`/api/posts/${params.id}/favorite-status`)
      ])

      if (likeResponse.ok) {
        const likeData = await likeResponse.json()
        setLiked(likeData.liked)
      }

      if (bookmarkResponse.ok) {
        const bookmarkData = await bookmarkResponse.json()
        setBookmarked(bookmarkData.favorited)
      }
    } catch (error) {
      console.error('Áä∂ÊÖãÁ¢∫Ë™ç„Ç®„É©„Éº:', error)
    }
  }, [params.id])

  const fetchPost = useCallback(async () => {
    try {
      const response = await fetch(`/api/posts/${params.id}`)
      if (response.ok) {
        const data = await response.json()
        setPost(data)
        
        // ÁõÆÊ¨°„ÇíÁîüÊàê
        if (data.content) {
          const toc = generateTocItems(data.content)
          setTocItems(toc)
        }
        
        // „ÅÑ„ÅÑ„Å≠„Éª„Éñ„ÉÉ„ÇØ„Éû„Éº„ÇØÁä∂ÊÖã„ÇíÁ¢∫Ë™ç
        if (session?.user?.id) {
          checkLikeAndBookmarkStatus()
        }
      }
    } catch (error) {
      console.error('ÊäïÁ®øÂèñÂæó„Ç®„É©„Éº:', error)
    } finally {
      setLoading(false)
    }
  }, [params.id, session?.user?.id, checkLikeAndBookmarkStatus])

  useEffect(() => {
    if (params.id) {
      fetchPost()
    }
  }, [params.id, fetchPost])

  const handleLike = async () => {
    if (!session?.user?.id) return

    try {
      const response = await fetch('/api/likes', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ postId: params.id })
      })

      if (response.ok) {
        const data = await response.json()
        setLiked(data.liked)
        
        // „ÅÑ„ÅÑ„Å≠Êï∞„ÇíÊõ¥Êñ∞
        if (post) {
          setPost({
            ...post,
            _count: {
              ...post._count,
              likes: post._count.likes + (data.liked ? 1 : -1)
            }
          })
        }
      }
    } catch (error) {
      console.error('„ÅÑ„ÅÑ„Å≠„Ç®„É©„Éº:', error)
    }
  }

  const handleBookmark = async () => {
    if (!session?.user?.id) return

    try {
      const response = await fetch('/api/favorites', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ postId: params.id })
      })

      if (response.ok) {
        const data = await response.json()
        setBookmarked(data.favorited)
      }
    } catch (error) {
      console.error('„Éñ„ÉÉ„ÇØ„Éû„Éº„ÇØ„Ç®„É©„Éº:', error)
    }
  }

  const handleDelete = async () => {
    if (!session?.user?.id || !post) return

    // Ê®©Èôê„ÉÅ„Çß„ÉÉ„ÇØ
    if (post.userId !== session.user.id && !session.user.isAdmin) {
      alert('ÂâäÈô§Ê®©Èôê„Åå„ÅÇ„Çä„Åæ„Åõ„Çì')
      return
    }

    if (!confirm('Êú¨ÂΩì„Å´„Åì„ÅÆÊäïÁ®ø„ÇíÂâäÈô§„Åó„Åæ„Åô„ÅãÔºü„Åì„ÅÆÊìç‰Ωú„ÅØÂèñ„ÇäÊ∂à„Åõ„Åæ„Åõ„Çì„ÄÇ')) {
      return
    }

    try {
      const response = await fetch(`/api/posts/${params.id}`, {
        method: 'DELETE',
      })

      if (response.ok) {
        router.push('/')
      } else {
        const data = await response.json()
        alert(data.error || 'ÂâäÈô§„Å´Â§±Êïó„Åó„Åæ„Åó„Åü')
      }
    } catch (error) {
      console.error('ÂâäÈô§„Ç®„É©„Éº:', error)
      alert('ÂâäÈô§„Å´Â§±Êïó„Åó„Åæ„Åó„Åü')
    }
  }

  const getTypeLabel = (type: string) => {
    switch (type) {
      case 'article':
        return 'Ë®ò‰∫ã'
      case 'prompt':
        return '„Éó„É≠„É≥„Éó„Éà'
      case 'conversation':
        return '‰ºöË©±'
      default:
        return type
    }
  }

  const getTypeColor = (type: string) => {
    switch (type) {
      case 'article':
        return styles.typeArticle
      case 'prompt':
        return styles.typePrompt
      case 'conversation':
        return styles.typeConversation
      default:
        return styles.typeDefault
    }
  }

  if (loading) {
    return (
      <div className={styles.container}>
        <div className={styles.loading}>Ë™≠„ÅøËæº„Åø‰∏≠...</div>
      </div>
    )
  }

  if (!post) {
    return (
      <div className={styles.container}>
        <div className={styles.loading}>ÊäïÁ®ø„ÅåË¶ã„Å§„Åã„Çä„Åæ„Åõ„Çì</div>
      </div>
    )
  }

  return (
    <div className={styles.container}>
      <div className={styles.contentWrapper}>
        {/* Â∑¶„Çµ„Ç§„Éâ„Éê„Éº */}
        <div className={styles.leftSidebar}>
          {session?.user?.id && (
            <>
              <button
                onClick={handleLike}
                className={`${styles.sideActionButton} ${styles.sideLikeButton} ${
                  liked ? styles.sideActionButtonActive : ''
                }`}
                title={`„ÅÑ„ÅÑ„Å≠ (${post._count.likes})`}
              >
                <span className={styles.sideActionIcon}>‚ô•</span>
                <span className={styles.sideActionCount}>{post._count.likes}</span>
              </button>
              
              <button
                onClick={handleBookmark}
                className={`${styles.sideActionButton} ${styles.sideBookmarkButton} ${
                  bookmarked ? styles.sideActionButtonActive : ''
                }`}
                title={bookmarked ? '„Éñ„ÉÉ„ÇØ„Éû„Éº„ÇØÊ∏à„Åø' : '„Éñ„ÉÉ„ÇØ„Éû„Éº„ÇØ'}
              >
                <span className={styles.sideActionIcon}>üîñ</span>
              </button>
              
              {/* X„Ç∑„Çß„Ç¢„Éú„Çø„É≥ */}
              {currentUrl && (
                <XShareButton
                  url={`${currentUrl}/posts/${post.id}`}
                  text={`${post.title} | omu`}
                  className={styles.sideShareButton}
                />
              )}
            </>
          )}
        </div>

        {/* „É°„Ç§„É≥„Ç≥„É≥„ÉÜ„É≥„ÉÑ */}
        <div className={styles.mainContent}>
          <article className={styles.article}>
        <div className={styles.header}>
          <div className={styles.headerTop}>
            <div className={styles.headerLeft}>
              <span className={`${styles.typeLabel} ${getTypeColor(post.type)}`}>
                {getTypeLabel(post.type)}
              </span>
              {post.platform && (
                <span className={styles.platform}>
                  {post.platform}
                </span>
              )}
            </div>
            <span className={styles.date}>
              {new Date(post.createdAt).toLocaleDateString('ja-JP')}
            </span>
          </div>
        </div>

        <h1 className={styles.title}>
          {post.title}
        </h1>


        <div className={styles.content}>
          <ReactMarkdown 
            components={MarkdownComponents}
            remarkPlugins={[remarkBreaks]}
          >
            {post.content}
          </ReactMarkdown>
        </div>

        <div className={styles.tags}>
          {post.postTags.map(({ tag }) => (
            <span
              key={tag.id}
              className={styles.tag}
            >
              #{tag.name}
            </span>
          ))}
        </div>
          </article>
        </div>

        {/* Âè≥„Çµ„Ç§„Éâ„Éê„Éº */}
        <div className={styles.sidebar}>
          {/* „É¶„Éº„Ç∂„ÉºÊÉÖÂ†± */}
          <div className={styles.authorInfo}>
            <div className={styles.authorCard}>
              <div className={styles.avatar}>
                {post.user.username[0]?.toUpperCase()}
              </div>
              <div className={styles.authorDetails}>
                <span className={styles.username}>
                  {post.user.username}
                </span>
                <div className={styles.dateInfo}>
                  <div className={styles.dateItem}>
                    <span className={styles.dateLabel}>ÊäïÁ®øÊó•:</span>
                    <span className={styles.dateValue}>
                      {new Date(post.createdAt).toLocaleDateString('ja-JP')}
                    </span>
                  </div>
                  <div className={styles.dateItem}>
                    <span className={styles.dateLabel}>Êõ¥Êñ∞Êó•:</span>
                    <span className={styles.dateValue}>
                      {new Date(post.updatedAt).toLocaleDateString('ja-JP')}
                    </span>
                  </div>
                </div>
                
                {/* Á∑®ÈõÜ„ÉªÂâäÈô§„Éú„Çø„É≥ÔºàËëóËÄÖ„Åæ„Åü„ÅØÁÆ°ÁêÜËÄÖ„ÅÆ„ÅøÔºâ */}
                {session?.user?.id && (session.user.id === post.userId || session.user.isAdmin) && (
                  <div className={styles.authorActions}>
                    <button
                      onClick={() => router.push(`/posts/${post.id}/edit`)}
                      className={styles.authorEditButton}
                    >
                      <span className={styles.buttonIcon}>‚úèÔ∏è</span>
                      Á∑®ÈõÜ
                    </button>
                    <button
                      onClick={handleDelete}
                      className={styles.authorDeleteButton}
                    >
                      <span className={styles.buttonIcon}>üóëÔ∏è</span>
                      ÂâäÈô§
                    </button>
                  </div>
                )}
              </div>
            </div>
          </div>

          {/* ÁõÆÊ¨° */}
          {tocItems.length > 0 && (
            <div className={styles.tableOfContents}>
              <h3 className={styles.tocTitle}>ÁõÆÊ¨°</h3>
              <ul className={styles.tocList}>
                {tocItems.map((item, index) => (
                  <li key={index} className={`${styles.tocItem} ${styles[`tocLevel${item.level}`]}`}>
                    <a 
                      href={`#${item.id}`}
                      onClick={(e) => {
                        e.preventDefault()
                        document.getElementById(item.id)?.scrollIntoView({ 
                          behavior: 'smooth' 
                        })
                      }}
                      className={styles.tocLink}
                    >
                      {item.text}
                    </a>
                  </li>
                ))}
              </ul>
            </div>
          )}
        </div>
      </div>
    </div>
  )
}